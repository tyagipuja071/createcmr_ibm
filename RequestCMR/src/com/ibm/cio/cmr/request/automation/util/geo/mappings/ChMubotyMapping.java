package com.ibm.cio.cmr.request.automation.util.geo.mappings;

/**
 * 
 * MappingDigester POJO for Switzerland/Leichtenstien MUBOTY mapping
 * 
 * @author RoopakChugh
 *
 */

public class ChMubotyMapping {
  private String ims;
  private String postalCdMin;
  private String postalCdMax;
  private String isu;
  private String ctc;
  private String muboty;

  public String getIms() {
    return ims;
  }

  public void setIms(String ims) {
    this.ims = ims;
  }

  public String getPostalCdMin() {
    return postalCdMin;
  }

  public void setPostalCdMin(String postalCdMin) {
    this.postalCdMin = postalCdMin;
  }

  public String getPostalCdMax() {
    return postalCdMax;
  }

  public void setPostalCdMax(String postalCdMax) {
    this.postalCdMax = postalCdMax;
  }

  public String getIsu() {
    return isu;
  }

  public void setIsu(String isu) {
    this.isu = isu;
  }

  public String getCtc() {
    return ctc;
  }

  public void setCtc(String ctc) {
    this.ctc = ctc;
  }

  public String getMuboty() {
    return muboty;
  }

  public void setMuboty(String muboty) {
    this.muboty = muboty;
  }

}
